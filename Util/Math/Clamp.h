//------------------------------------------------------------------------------
//	Clamp
//------------------------------------------------------------------------------
template<class T>
inline T clamp(T val, T lo, T hi)
{
	if (val < lo)
		return lo;

	if (val > hi)
		return hi;

	return val;
}
