//------------------------------------------------------------------------------
//	RoundUp
//------------------------------------------------------------------------------
template<class T>
T roundUp(T val, T roundTo)
{
	const T leftOver = val % roundTo;

	if (leftOver != 0)
		return val + (roundTo - leftOver);

	return val;
}
